export function areacuadrado(lado) {
    return lado * lado;
}

export function areaTriangulo(base, altura) {
    return (base * altura) / 2;
}

export function areaCirculo(radio) {
    return Math.PI * radio * radio;
}

export function areaRectangulo(ancho, alto) {
    return ancho * alto;
}



